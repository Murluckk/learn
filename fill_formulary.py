# ---------- HELPERS ----------

def fill_paragraph_blanks(p, values):
    """Fill `len(values)` consecutive underscore-blanks inside paragraph `p`.
    A 'blank' = a maximal run of underscore characters spanning 1+ runs.
    """
    runs = p.runs
    # Build flat list of (run_idx, char_idx, char) for analysis.
    spans = []  # list of (start_run, start_char, end_run, end_char)  [inclusive]
    cur_start = None
    for ri, r in enumerate(runs):
        for ci, ch in enumerate(r.text):
            if ch == '_':
                if cur_start is None:
                    cur_start = (ri, ci)
                last = (ri, ci)
            else:
                if cur_start is not None:
                    spans.append((cur_start, last))
                    cur_start = None
        # end of run – do not break a span; underscores can continue in next run
    if cur_start is not None:
        spans.append((cur_start, last))

    if not spans:
        return

    if len(values) > len(spans):
        values = values[: len(spans)]

    # Walk spans in reverse order to keep earlier indices valid.
    # For each span: replace underscores with provided value (only first span run keeps text;
    # other runs in span get their underscore portion stripped).
    # Map run-by-run: we need to know which spans intersect each run.
    # Simpler approach: rebuild each run's text with substitutions.
    # Build mark array: for each (ri, ci), mark which span it's in (or None).
    span_id = {}
    for sid, (a, b) in enumerate(spans):
        ar, ac = a
        br, bc = b
        if ar == br:
            for ci in range(ac, bc + 1):
                span_id[(ar, ci)] = sid
        else:
            # ar < br
            for ci in range(ac, len(runs[ar].text)):
                span_id[(ar, ci)] = sid
            for r_between in range(ar + 1, br):
                for ci in range(len(runs[r_between].text)):
                    span_id[(r_between, ci)] = sid
            for ci in range(0, bc + 1):
                span_id[(br, ci)] = sid

    used_spans = set()
    for ri, r in enumerate(runs):
        new_chars = []
        i = 0
        txt = r.text
        while i < len(txt):
            sid = span_id.get((ri, i))
            if sid is None:
                new_chars.append(txt[i])
                i += 1
            else:
                # find end of this span chunk inside this run
                j = i
                while j < len(txt) and span_id.get((ri, j)) == sid:
                    j += 1
                # If this span has a value and we haven't placed it yet, place at first run
                if sid < len(values) and sid not in used_spans:
                    new_chars.append(str(values[sid]))
                    used_spans.add(sid)
                # else nothing (we drop the underscores)
                i = j
        r.text = ''.join(new_chars)


def cell_paragraphs(cell):
    return cell.paragraphs


def write_cell(cell, text, append=False):
    """Write `text` to a cell. If multiple lines, split across paragraphs."""
    lines = text.split('\n')
    paras = list(cell.paragraphs)
    if not append:
        # clear existing text but keep first paragraph
        for p in paras:
            for r in list(p.runs):
                r.text = ''
    # Use first paragraph for first line, add new paragraphs for remaining.
    first_p = paras[0]
    # ensure at least one run exists
    if first_p.runs:
        first_p.runs[0].text = lines[0]
    else:
        first_p.add_run(lines[0])
    for line in lines[1:]:
        new_p = cell.add_paragraph()
        # copy formatting from first paragraph if possible
        if first_p.style is not None:
            new_p.style = first_p.style
        new_p.add_run(line)


def fill_blank_paragraphs_after(start_paragraph, lines):
    """Write `lines` (list of strings) into the paragraphs that follow
    `start_paragraph` (the section header), reusing empty paragraphs first
    and inserting new ones (with the same style as the section's body) before
    the next non-empty sibling when needed.
    """
    from docx.text.paragraph import Paragraph
    from copy import deepcopy

    p_elem = start_paragraph._p
    parent = p_elem.getparent()
    siblings = list(parent.iterchildren())
    try:
        start_idx = siblings.index(p_elem)
    except ValueError:
        return

    # Collect available empty paragraphs after the header (until next
    # non-empty paragraph or non-paragraph element).
    blanks = []
    next_non_blank = None
    for sibling in siblings[start_idx + 1:]:
        tag = sibling.tag.split('}')[-1]
        if tag != 'p':
            next_non_blank = sibling
            break
        text_now = ''.join(sibling.itertext())
        if text_now.strip():
            next_non_blank = sibling
            break
        blanks.append(sibling)

    # Template paragraph for inserting new ones – use the last blank if any,
    # otherwise the start_paragraph (header) – we'll strip its content.
    template = blanks[-1] if blanks else p_elem

    # Helper to write a single line into a paragraph element.
    def write_line(p_xml, text):
        p_obj = Paragraph(p_xml, start_paragraph._parent)
        # Remove any existing runs (paragraph might already have empty runs)
        for r in list(p_obj.runs):
            r._element.getparent().remove(r._element)
        if text:
            run = p_obj.add_run(text)
            try:
                src_run = start_paragraph.runs[0]
                if src_run.font.name:
                    run.font.name = src_run.font.name
                run.bold = False
            except Exception:
                pass

    # Place lines into existing blanks first.
    used = 0
    for sibling in blanks:
        if used >= len(lines):
            break
        write_line(sibling, lines[used])
        used += 1

    # Insert new paragraphs for the remaining lines.
    if used < len(lines):
        anchor = next_non_blank if next_non_blank is not None else None
        for line in lines[used:]:
            new_p = deepcopy(template)
            # Strip all runs from the cloned template
            for r in list(new_p.findall(qn('w:r'))):
                new_p.remove(r)
            # Insert before anchor (or append at end of parent)
            if anchor is not None:
                anchor.addprevious(new_p)
            else:
                parent.append(new_p)
            write_line(new_p, line)


def get_paragraphs_in_order(doc):
    """Return list of paragraphs in body order (only top-level, not inside tables)."""
    body = doc.element.body
    paragraphs = []
    p_index = 0
    for child in body.iterchildren():
        tag = child.tag.split('}')[-1]
        if tag == 'p':
            # find the paragraph object that wraps this
            paragraphs.append(doc.paragraphs[p_index])
            p_index += 1
    return paragraphs


