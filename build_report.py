def add_table(doc, header, rows, col_widths_cm=None, caption=None):
    if caption:
        c = doc.add_paragraph()
        c.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.LEFT
        c.paragraph_format.first_line_indent = Cm(0)
        c.paragraph_format.space_before = Pt(6)
        c.paragraph_format.space_after = Pt(3)
        r = c.add_run(caption)
        set_run_font(r, size_pt=14)
    t = doc.add_table(rows=1 + len(rows), cols=len(header))
    t.alignment = WD_ALIGN_PARAGRAPH.CENTER
    hdr = t.rows[0].cells
    for i, h in enumerate(header):
        hdr[i].text = ""
        p = hdr[i].paragraphs[0]
        p.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r = p.add_run(h)
        set_run_font(r, size_pt=12, bold=True)
        hdr[i].vertical_alignment = WD_ALIGN_VERTICAL.CENTER
    for i, row in enumerate(rows, start=1):
        cells = t.rows[i].cells
        for j, val in enumerate(row):
            cells[j].text = ""
            p = cells[j].paragraphs[0]
            p.paragraph_format.alignment = (
                WD_ALIGN_PARAGRAPH.LEFT if j == 0 else WD_ALIGN_PARAGRAPH.CENTER
            )
            r = p.add_run(str(val))
            set_run_font(r, size_pt=12)
            cells[j].vertical_alignment = WD_ALIGN_VERTICAL.CENTER
    if col_widths_cm:
        for i, w in enumerate(col_widths_cm):
            for row in t.rows:
                row.cells[i].width = Cm(w)
    add_table_borders(t)
    return t


