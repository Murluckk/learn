def set_cell_border(cell, **kwargs):
    """Add borders to a single cell."""
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    tcBorders = tcPr.find(qn("w:tcBorders"))
    if tcBorders is None:
        tcBorders = OxmlElement("w:tcBorders")
        tcPr.append(tcBorders)
    for edge in ("top", "left", "bottom", "right"):
        if edge in kwargs:
            tag = OxmlElement(f"w:{edge}")
            tag.set(qn("w:val"), "single")
            tag.set(qn("w:sz"), "6")
            tag.set(qn("w:color"), "000000")
            tcBorders.append(tag)


def add_table_borders(table):
    for row in table.rows:
        for cell in row.cells:
            set_cell_border(cell, top=True, left=True, bottom=True, right=True)


def set_run_font(run, size_pt=14, bold=False, italic=False, name="Times New Roman"):
    run.font.name = name
    run.font.size = Pt(size_pt)
    run.bold = bold
    run.italic = italic
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn("w:rFonts"))
    if rFonts is None:
        rFonts = OxmlElement("w:rFonts")
        rPr.append(rFonts)
    rFonts.set(qn("w:ascii"), name)
    rFonts.set(qn("w:hAnsi"), name)
    rFonts.set(qn("w:cs"), name)
    rFonts.set(qn("w:eastAsia"), name)


def add_p(doc, text="", *, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
          first_line=Cm(1.25), bold=False, italic=False, size=14,
          space_after=Pt(0), line_spacing=1.5, center=False):
    p = doc.add_paragraph()
    pf = p.paragraph_format
    pf.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else align
    if not center and first_line is not None:
        pf.first_line_indent = first_line
    pf.line_spacing = line_spacing
    pf.space_after = space_after
    pf.space_before = Pt(0)
    if text:
        r = p.add_run(text)
        set_run_font(r, size_pt=size, bold=bold, italic=italic)
    return p


